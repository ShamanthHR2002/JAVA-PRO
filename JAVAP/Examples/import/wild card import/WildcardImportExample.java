package com.shamanth.example.Wildcard;

import java.util.*;

public class WildcardImportExample {
	  public static void main(String[] args) {
	       
	        List<String> list = new ArrayList<>();  
	        list.add("Apple");
	        list.add("Banana");

	        for (String fruit : list) {
	            System.out.println(fruit);
	        }
	    }

}
