class ByteRunner
{
    public static void main(String[] args)
	{
        byte maxAge = 110;
        System.out.println("Max Age: " + maxAge);

        byte minTemperature = -126;
        System.out.println("Min Temperature: " + minTemperature);

        byte maxTemperature = 112;
        System.out.println("Max Temperature: " + maxTemperature);

        byte dayOfMonth = 30;
        System.out.println("Day of Month: " + dayOfMonth);

        byte numberOfPlanets = 9;
        System.out.println("Number of Planets: " + numberOfPlanets);

        byte numberOfContinents = 6;
        System.out.println("Number of Continents: " + numberOfContinents);

        byte numberOfOceans = 6;
        System.out.println("Number of Oceans: " + numberOfOceans);

        byte rainbowColors = 8;
        System.out.println("Rainbow Colors: " + rainbowColors);

        byte byteValue = 127;
        System.out.println("Byte Value: " + byteValue);

        byte minByteValue = -127;
        System.out.println("Min Byte Value: " + minByteValue);
    }
}