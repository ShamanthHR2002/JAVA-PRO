package com.xworkz.interfacepro;

public class CityTraffic implements TrafficRule {

	public void followSpeedLimit() {
		System.out.println("Follow the speed limit as per the traffic rules.");
	}

	public void wearSeatbelt() {
		System.out.println("Always wear a seatbelt while driving.");
	}

	public void obeyTrafficSignals() {
		System.out.println("Obey all traffic signals at intersections.");
	}

	public void stopForPedestrians() {
		System.out.println("Stop for pedestrians at zebra crossings.");
	}

	public void useIndicators() {
		System.out.println("Use indicators while turning.");
	}

	public void maintainLaneDiscipline() {
		System.out.println("Maintain proper lane discipline while driving.");
	}

	public void avoidUsingMobileWhileDriving() {
		System.out.println("Avoid using mobile phones while driving.");
	}

	public void followNoParkingSigns() {
		System.out.println("Follow no-parking signs in designated areas.");
	}

	public void followOneWay() {
		System.out.println("Always follow the one-way road directions.");
	}

	public void doNotDrinkAndDrive() {
		System.out.println("Do not drink and drive.");
	}

	public void avoidOvertakingFromLeft() {
		System.out.println("Avoid overtaking vehicles from the left.");
	}

	public void followZebraCrossing() {
		System.out.println("Give priority to pedestrians at zebra crossings.");
	}

	public void giveWayToEmergencyVehicles() {
		System.out.println("Always give way to emergency vehicles.");
	}

	public void followHelmetRule() {
		System.out.println("Always wear a helmet when riding a two-wheeler.");
	}

	public void followChildSafetyLaws() {
		System.out.println("Ensure child passengers are seated in safety seats.");
	}
}
